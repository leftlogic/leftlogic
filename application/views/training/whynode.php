<h1>Why your boss should send to you to the Real-time Node &amp; HTML5 workshop</h1>

<p>The web used to be static. Single page, click, reload. Then came Ajax and we embraced it. The web was now dynamic. We've moved on. Our expectations are higher, we want information in an instant. The web has moved on to <strong>real-time.</strong></p>

<p>Adding real-time to your application either requires insane talant that understands Comet... or you look at how <a href="http://nodejs.org">Node.js</a> and native HTML5 browser technology can do this for you.</p>

<p>Making use of <a href="http://html5demos.com/web-socket">WebSockets</a> is easy enough, but without the server technology you have nothing.</p>

<p>This workshop, along with covering the fundamentals of how Node works and how web servers work, walks through how simple it is to use Node to create real-time servers.</p>

<h2>Is Node <em>too</em> new?</h2>

<p>Is node too new for your company? Well, Microsoft, Ebay, Linked and Yahoo! to name a few, are all using Node in production. It's cross platform, so that means is the same code and development experience whether you prefer to code on Windows on a Mac or on a Linux flavoured machine.</p>

<p>This workshop will give you the tools you need to add real-time services to your application from day one.</p>

